"""문서 유형 4종. 원본은 docs/type.md 이고 이 파일은 그것을 코드로 옮긴 것이다.

독자(audience)를 따로 묻지 않는 이유: "누가 읽는가"는 결국 "어떤 유형의
문서인가"에 담겨 있다. 튜토리얼을 찾는 사람은 처음 접하는 사람이고, 참조
문서를 찾는 사람은 이미 쓸 줄 아는 사람이다. 질문을 하나로 줄인다.

템플릿은 작가 프롬프트에 그대로 들어간다. 대괄호 안은 각 섹션에 무엇을
쓰라는 안내문이고, 작가는 이를 실제 내용으로 바꿔야 한다.
"""

from __future__ import annotations

from dataclasses import dataclass


@dataclass(frozen=True)
class DocType:
    name: str
    when: str  # 언제 이 유형을 쓰는지. 화면의 선택지 힌트로도 쓰인다.
    template: str  # 섹션 골격. 작가 프롬프트에 그대로 삽입된다.


_TUTORIAL = DocType(
    name="학습 중심 문서",
    when="새로운 기술이나 도구를 처음 접해서 간단히 어떤 흐름인지 알고 싶을 때",
    template="""# [튜토리얼 제목]

## 목표

[이 튜토리얼을 따라하고 나면 독자가 달성할 목표를 간략히 설명하세요.]

## 사전 요구사항

- [이 튜토리얼을 따라 하기 전에 필요한 사전 지식, 설치해야 할 도구를 정리하세요. 없다면 생략해도 돼요.]
- [예: Node.js 버전, API 키, 필수 패키지 등]

## 단계별 가이드

### [첫 번째 단계 제목]

[이 단계에서 해야 할 작업을 설명하고 코드 예제 또는 UI 캡처를 포함하세요.]

### [두 번째 단계 제목]

[다음 단계에서 수행할 작업을 설명하세요.]

### [필요한 만큼 단계를 더 추가]

### 최종 결과 확인

[독자가 목표를 달성했을 때, 어떤 결과가 나오는지 설명하세요.]""",
)

_TROUBLESHOOT = DocType(
    name="문제 해결 문서",
    when="배경 지식이 있는 상태에서 기술이나 도구를 사용하다 생기는 특정한 문제를 해결하고 싶을 때",
    template="""# [문제 해결 문서 제목]

## 문제 정의

[독자가 겪을 수 있는 문제 상황을 설명하세요.]

## 사전 요구사항

[문제를 해결하기 전에 필요한 환경 설정이나 필수 조건을 정리하세요. 없다면 생략해도 돼요.]

## 해결 방법

### [첫 번째 해결 방법]

[첫 번째 해결 방법을 설명하세요.]

### [두 번째 해결 방법]

[다른 해결 방법이 있다면 추가하세요.]

### 문제 해결 후 확인 방법

[문제가 해결된 후의 모습이나 확인하는 방법을 설명하세요.]""",
)

_REFERENCE = DocType(
    name="참조 문서",
    when="이미 기본적인 작동 방법을 알고 있는 상태에서 특정 기능이나 API 사용법을 확인해서 적용하고 싶을 때",
    template="""# [참조 문서 제목]

## 개요

[이 요소가 무엇이며, 언제 사용하는지, 어떤 가치를 제공하는지 설명하세요.]

## 속성 및 옵션

| 속성명 | 타입 | 기본값 | 설명 |
|--------|------|--------|------|
| prop1 | string | "default" | 이 속성은 ... |

## 시그니처

[예제 코드]

## 반환 값

[이 함수나 API가 반환하는 값을 설명하세요.]

## 사용 예제

[어떤 상황에서 사용하는지 구체적인 예시와 함께 예제 코드를 알려주세요.]""",
)

_EXPLANATION = DocType(
    name="설명 문서",
    when="개념, 원리, 배경 지식을 깊이 이해하고 싶을 때 — 왜 이 기술이 등장했는지, 어떤 문제를 해결하는지",
    template="""# [개념명]

## 개념 소개

[이 개념이 무엇인지 간략히 설명하세요.]

## 등장 배경

[이 개념이 왜 등장했는지, 어떤 문제를 해결하는지 정리하세요.]

## 활용

[실제 프로젝트에서 이 개념이 어떻게 사용되는지 설명하세요.]""",
)

DOC_TYPES: dict[str, DocType] = {
    d.name: d for d in (_TUTORIAL, _TROUBLESHOOT, _REFERENCE, _EXPLANATION)
}

DOC_TYPE_NAMES = list(DOC_TYPES)
